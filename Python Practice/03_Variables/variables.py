# Example of defining variables in Python

# Integer variable
age = 25  # storing age of a person

# Floating-point variable
height = 1.75  # storing height in meters

# String variable
name = "John Doe"  # storing name of a person

# Boolean variable
is_student = True  # storing whether a person is a student or not

# List variable (collection of items)
grades = [85, 92, 78, 90, 88]  # storing a list of grades

# Dictionary variable (key-value pairs)
person_info = {'name': 'Alice', 'age': 30, 'city': 'New York'}  # storing information about a person

# Tuple variable (immutable collection of items)
coordinates = (3.14, 2.71)  # storing coordinates

# Set variable (collection of unique items)
skills = {'Python', 'JavaScript', 'SQL'}  # storing a set of programming skills

# NoneType variable (represents absence of a value)
result = None  # storing the result of an operation, which hasn't been computed yet

# Outputting values to demonstrate usage
print(f"Name: {name}, Age: {age}, Height: {height}")
print(f"Is student? {is_student}")
print(f"Grades: {grades}")
print(f"Person info: {person_info}")
print(f"Coordinates: {coordinates}")
print(f"Skills: {skills}")
print(f"Result: {result}")
