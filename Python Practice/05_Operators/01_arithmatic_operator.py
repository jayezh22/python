# Assigning values to variables
a = 10  # Variable 'a' is assigned the value 10
b = 20  # Variable 'b' is assigned the value 20

# Addition
print(a + b)  # Adding 'a' and 'b'. Output: 30

# Subtraction
print(a - b)  # Subtracting 'b' from 'a'. Output: -10

# Multiplication
print(a * b)  # Multiplying 'a' and 'b'. Output: 200

# Division (floating-point)
print(a / b)  # Dividing 'a' by 'b'. Output: 0.5

# Integer Division (floor division)
print(a // b)  # Floor division of 'a' by 'b'. Output: 0 (because 10 divided by 20 is less than 1)

# Exponentiation
print(a ** b)  # 'a' raised to the power of 'b'. Output: 100000000000000000000

