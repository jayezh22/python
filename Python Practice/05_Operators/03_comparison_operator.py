a = 10  # Assigning value 10 to variable 'a'
b = 20  # Assigning value 20 to variable 'b'

# Equal to
print(a == b)  # Output: False, because 10 is not equal to 20

# Not equal to
print(a != b)  # Output: True, because 10 is not equal to 20

# Greater than
print(a > b)   # Output: False, because 10 is not greater than 20

# Less than
print(a < b)   # Output: True, because 10 is less than 20

# Greater than or equal to
print(a >= b)  # Output: False, because 10 is not greater than or equal to 20

# Less than or equal to
print(a <= b)  # Output: True, because 10 is less than or equal to 20
