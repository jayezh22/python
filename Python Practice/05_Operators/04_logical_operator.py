a = 10  # Assigning value 10 to variable 'a'
b = 20  # Assigning value 20 to variable 'b'

# Logical AND operation
print(a < 5 & a < 10)  
# Output: True
# Explanation: This is evaluated as (a < 5) and (a < 10). 
# Since 10 is not less than 5, the expression (a < 5) evaluates to False,
# and hence, (a < 5) and (a < 10) evaluates to False and False which is False.

