# Define a list of fruits
fruits = ['apple', 'banana', 'cherry']

# Start a for loop that will iterate over each fruit in the list
for fruit in fruits:
    # Print the current fruit being processed in the loop
    print(fruit)
else:
    # This else clause will be executed when the loop finishes normally (i.e., without encountering a break statement)
    # It will print a message indicating that there are no more fruits
    print("No more fruits!")