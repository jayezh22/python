# Integer
age = 25  # Age of a person
print(f"Age: {age}, Type: {type(age)}")

# Float
height = 1.75  # Height in meters
print(f"Height: {height}, Type: {type(height)}")

# String
name = "John Doe"  # Name of a person
print(f"Name: {name}, Type: {type(name)}")

# Boolean
is_student = True  # Indicates whether a person is a student or not
print(f"Is student? {is_student}, Type: {type(is_student)}")

# List
grades = [85, 92, 78, 90, 88]  # List of grades
print(f"Grades: {grades}, Type: {type(grades)}")

# Tuple
coordinates = (3.14, 2.71)  # Coordinates (e.g., latitude and longitude)
print(f"Coordinates: {coordinates}, Type: {type(coordinates)}")

# Dictionary
person_info = {'name': 'Alice', 'age': 30, 'city': 'New York'}  # Information about a person
print(f"Person info: {person_info}, Type: {type(person_info)}")

# Set
skills = {'Python', 'JavaScript', 'SQL'}  # Set of programming skills
print(f"Skills: {skills}, Type: {type(skills)}")

# NoneType
result = None  # Represents absence of a value
print(f"Result: {result}, Type: {type(result)}")

