# Initialize a variable i to 0
i = 0

# Start a while loop that will continue to execute as long as i is less than 5
while i < 5:
    # Print the current value of i
    print(i)
    
    # Increment i by 1
    i += 1
else:
    # This else clause will be executed when the loop finishes normally, i.e., when i reaches 5
    print("Loop finished normally")
