# Initialize a variable i to 0
i = 0

# Use a while loop to iterate as long as i is less than 6
while i < 6:
    # Check if i is equal to 3
    if i == 3:
        # If i is 3, break out of the loop immediately
        break
    # Print the current value of i
    print(i)
    # Increment i by 1
    i += 1