# Assigning a value to the variable a
a = 2  # assigning 2 to variable a

# Conditional statement to check if a is even or odd
if a % 2 == 0:  # checking if a is divisible by 2 (i.e., even)
    print("even")  # this condition is true, so it will print "even"
else:  # if the above condition is false, then this block will execute
    print("odd")  # but in this case, the above condition is true, so this won't execute